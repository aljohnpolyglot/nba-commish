export function pw(arr: any[], wf: (item: any) => number) {
  const ws = arr.map(wf);
  const t = ws.reduce((a, b) => a + b, 0);
  if (!t) return arr[~~(Math.random() * arr.length)];
  let r = Math.random() * t;
  for (let i = 0; i < arr.length; i++) {
    r -= ws[i];
    if (r <= 0) return arr[i];
  }
  return arr[arr.length - 1];
}

export function pick<T>(a: T[]): T {
  return a[~~(Math.random() * a.length)];
}

// Returns a distance string based on shot type 
// and Limitless Range level
export function getThreePointDistance(
  pos: string,
  limitlessLevel: 'HOF' | 'Gold' | 'Silver' | 'Bronze' | null
): string | null {

  // Only show distance ~40% of the time for 
  // regular shooters — keeps it from being 
  // repetitive
  const showDistance = Math.random() < 0.40;
  if (!showDistance && !limitlessLevel) return null;

  if (limitlessLevel === 'HOF') {
    // HOF fires almost always with distance flavor
    if (Math.random() > 0.15) {
      return pick([
        'from 35 feet',
        'from logo range',
        'from 37 feet out',
        'from near halfcourt',
        'from 38 feet',
        'from way downtown',
        'from 36 feet',
      ]);
    }
  }

  if (limitlessLevel === 'Gold') {
    return pick([
      'from 32 feet',
      'from 31 feet',
      'from well beyond the arc',
      'from 33 feet out',
      'from several steps back',
    ]);
  }

  if (limitlessLevel === 'Silver') {
    return pick([
      'from a step behind the arc',
      'from just beyond the line',
      'from 26 feet',
      'from 27 feet out',
    ]);
  }

  if (limitlessLevel === 'Bronze') {
    return pick([
      'from a step behind the arc',
      'from just beyond the line',
      'from 26 feet',
    ]);
  }

  // No Limitless Range — standard arc distance
  // Only fires 40% of the time (showDistance check)
  if (!showDistance) return null;

  if (pos === 'F' || pos === 'C') {
    return pick([
      'from 25 feet',
      'from the corner',
      'from the wing',
      'from 26 feet',
      null, // sometimes no distance
      null,
    ]) as string | null;
  }

  // Guards — standard, show occasionally
  return pick([
    'from 26 feet',
    'from 27 feet',
    'from the wing',
    'from the top of the arc',
    null,
    null,
    null, // nulls reduce frequency for guards
  ]) as string | null;
}
